#include <stdio.h>
#include <string.h>

int main() {
    //printf 0 to 4
    for(int i=0; i<10;i+=2)
    printf("%d\n",i);
    
    //print 0 2 4 6 8...
    for(int i=0; i<10;i+=2)
    printf("%d\n",i);
    
    //print 1 3 5 7 9....
    for(int i=1; i<10;i+=2)
    printf("%d\n",i);
    
    return 0;
}